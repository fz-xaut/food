package xaut.meal.system.pojo;

public class MyOrder {
    
    private int orderId;
    private String foodSellWindow;
    private String foodName;
    private String foodTaste;
    private String foodPrice;
    private String stuName;
    private String stuCall;
    public int getOrderId() {
        return orderId;
    }
    public void setOrderId(int orderId) {
        this.orderId = orderId;
    }
    public String getFoodSellWindow() {
        return foodSellWindow;
    }
    public void setFoodSellWindow(String foodSellWindow) {
        this.foodSellWindow = foodSellWindow;
    }
    public String getFoodName() {
        return foodName;
    }
    public void setFoodName(String foodName) {
        this.foodName = foodName;
    }
    public String getFoodTaste() {
        return foodTaste;
    }
    public void setFoodTaste(String foodTaste) {
        this.foodTaste = foodTaste;
    }
    public String getFoodPrice() {
        return foodPrice;
    }
    public void setFoodPrice(String foodPrice) {
        this.foodPrice = foodPrice;
    }
    public String getStuName() {
        return stuName;
    }
    public void setStuName(String stuName) {
        this.stuName = stuName;
    }
    public String getStuCall() {
        return stuCall;
    }
    public void setStuCall(String stuCall) {
        this.stuCall = stuCall;
    }
    
}
